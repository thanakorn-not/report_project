/* ===============================
   SQL
================================ */
$select = [];
foreach ($columns as $c) {
    if ($c === 'district_name') {
        $select[] = "d.district_name";
    } else {
        $select[] = "r.$c";
    }
}

$sql = "
    SELECT " . implode(',', $select) . "
    FROM {$table} r
    LEFT JOIN districts d ON r.district_id = d.id
    WHERE 1
";

$params = [];

// filter à¸›à¸à¸•à¸´
if ($year !== '') {
    $sql .= " AND r.year = :year";
    $params[':year'] = $year;
}
